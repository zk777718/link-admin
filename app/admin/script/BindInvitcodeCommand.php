    protected function configure()
    {
        $this->setName('BindInvitcodeCommand')
            ->setDescription('BindInvitcodeCommand')
            ->addArgument('start', Argument::OPTIONAL, "start", '')
            ->addArgument('end', Argument::OPTIONAL, "end", '');

        $marketchannelModel = MarketChannelModel::getInstance()->getModel();
        $count = $marketchannelModel->count();
        if (count($this->invitcodes_map) != $count) {
            $this->invitcodes_map = $marketchannelModel->where('invitcode', '<>', '')->column('id', 'invitcode');
        }

        $this->date = date("Y-m-d");
        $this->time = date("Y-m-d H:i:s");
    }

    /**
     *执行
     */
    protected function execute(Input $input, Output $output)
    {
        $start = trim($input->getArgument('start'));
        $end = trim($input->getArgument('end'));

        $this->bindUsers();

        echo "start======>" . $this->time . PHP_EOL;
        echo "finish======>" . date("Y-m-d H:i:s") . PHP_EOL;
    }

    /**
     *处理每日推广数据
     */
    protected function bindUsers()
    {
        if ($this->bind_users) {
            foreach ($this->bind_users as $uid => $invitcode) {
                $this->dealData($uid, $invitcode);
            }
        }
    }

    /*
     * 登陆数据
     */
    protected function getUserInfo($uid)
    {
        return MemberModel::getInstance()->getModel($uid)->where('id', $uid)->value('invitcode');
    }

    protected function dealData($uid, $invitcode)
    {
        try {
            echo "处理用户ID:" . $uid . PHP_EOL;
            $invitcodes_map = $this->invitcodes_map;
            //更新基础数据渠道信息
            //调用接口更新用户
            $redis = RedisCommon::getInstance()->getRedis();
            $admin_token = $redis->get('admin_token_' . $this->admin_id);

            $params = [
                'operatorId' => $this->admin_id,
                'token' => $admin_token,
                'userId' => (int) $uid,
                'datas' => json_encode([
                    'invitcode' => $invitcode,
                ]),
            ];

            ApiService::getInstance()->curlApi(ApiUrlConfig::$set_user_info, $params);

            $bind_invitcode = $invitcodes_map[$invitcode] ?? 0;
            BiUserStats5MinsModel::getInstance()->getModel()->where('uid', $uid)->update(['promote_channel' => $bind_invitcode]);
            BiUserStats1DayModel::getInstance()->getModel()->where('uid', $uid)->update(['promote_channel' => $bind_invitcode]);
            BiDaysUserChargeModel::getInstance()->getModel()->where('uid', $uid)->update(['promote_channel' => $bind_invitcode]);
            BiDaysUserChargeNewModel::getInstance()->getModel()->where('uid', $uid)->update(['promote_channel' => $bind_invitcode]);

        } catch (Throwable $e) {
            Log::error(sprintf('VipHandler::calUserDiamond ex=%d:%s trace=%s', $e->getCode(), $e->getMessage(), $e->getTraceAsString()));
            throw $e;
        }
    }
